    if l is None:
        return False
    l = l.lower()
    if l in united_nations_style_languages:
        return True
    if l in popular_languages_in_russian:
        return True
    if l in popular_languages_in_english:
        return True
    if l in popular_languages_in_native:
        return True
    if len(l) == 3:
        try:
            if languages.get(part3=l) is not None:
                return True
        except Exception as exp:
            pass
    return False
